    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
                  gs=new GetterSetter();
                  String name=  jTextField1.getText();
                  String pswrd=jPasswordField1.getText();
                  DbOperation dbc=new DbOperation();
                  ResultSet rs=dbc.loginConnection(name, pswrd);
                  String module1 = null;
                  String EmpName1 = null;
                 
                  
                  
                  try
                  {
                  if(rs.next())
                  {
                  gs.setname(rs.getString("NAME"));
                  gs.setEmail(rs.getString("EMAIL"));
                  gs.setGender(rs.getString("gender"));
                  gs.setMblnum(rs.getString("MOBILE_NUM"));
                  gs.setPswrd(rs.getString("PASSWORD"));
                  
                  
                      
                  module1=rs.getString("MODULE");
                  EmpName1=rs.getString("NAME");
                  if(module1.equals("ADMIN"))
                  {
                  
                  new AdminPanel().setVisible(true);
                  setVisible(false);

                  }
                  else if(module1.equals("EMPLOYEE"))
                  {
                  new EmployeePanel(gs).setVisible(true);
                     setVisible(false);
                  
                  }
                 
                  }
                  else
                  {
                  JOptionPane.showMessageDialog(rootPane, "Incorrect Password");
                  }
                  }
                  catch(Exception e)
                  {
                      System.out.println(e);
                  }
                           
    }//GEN-LAST:event_jButton1ActionPerformed
        
